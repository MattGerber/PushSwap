#include "libft.h"

char	*ft_convert(char *s, int n, int i)
{
	int c;

	c = 0;
	if (n == INTMIN)
		s = ft_strdup("-2147483648");
	else
	{
		if (n < 0)
		{
			c = 1;
			n *= -1;
			i++;
		}
		while (i > 0)
		{
			s[--i] = (n % 10 + '0');
			n = n / 10;
		}
		if (c == 1)
			s[0] = '-';
	}
	return (s);
}
